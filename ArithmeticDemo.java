public class ArithmeticDemo {
    public static void main(String[] args){
        int result = 2;
        result ++;
        System.out.println(result);

        result--;
        System.out.println(result);

        result *=2;
        System.out.println(result);

        result /=2;
        System.out.println(result);

        result +=8;
        System.out.println(result);

        result %=7;
        System.out.println(result);

    }
}
